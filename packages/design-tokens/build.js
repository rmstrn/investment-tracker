/**
 * Build script for @investment-tracker/design-tokens
 *
 * Reads tokens from ./tokens/** (DTCG format — $value, $type, {ref.path}) and emits:
 *   build/css/tokens.css       — CSS custom properties. :root = primitives + light-semantic;
 *                                .dark = overrides for semantic.
 *   build/tailwind/tokens.js   — Tailwind-ready tokens object. Primitives concrete,
 *                                semantic colors as var() refs so dark mode switches.
 *   build/swift/Tokens.swift   — Swift enum with adaptive Colors (light/dark) for iOS.
 *   build/js/tokens.js         — Flat JS object of all tokens (light mode values).
 *   build/js/brand.js          — Product name, domain placeholders, legal meta.
 *
 * The tokens directory uses DTCG format so we can swap in Style Dictionary later
 * without touching the tokens themselves.
 *
 * Run: `npm run build`  (no deps needed — pure Node)
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = __dirname;
const BUILD_DIR = path.join(ROOT, 'build');

// ----------------------------------------------------------
// Token loading + ref resolution (DTCG-compatible)
// ----------------------------------------------------------

function readJson(relPath) {
  return JSON.parse(fs.readFileSync(path.join(ROOT, relPath), 'utf8'));
}

/** Deep-merge b into a (plain objects only). */
function merge(a, b) {
  for (const [k, v] of Object.entries(b)) {
    if (v && typeof v === 'object' && !Array.isArray(v) && a[k] && typeof a[k] === 'object') {
      merge(a[k], v);
    } else {
      a[k] = v;
    }
  }
  return a;
}

function loadTree(files) {
  const tree = {};
  for (const f of files) merge(tree, readJson(f));
  return tree;
}

/** Walks a token tree and returns a flat list of leaf tokens. */
function flatten(tree, prefix = []) {
  const out = [];
  for (const [k, v] of Object.entries(tree)) {
    if (k.startsWith('$')) continue;
    if (v && typeof v === 'object' && !Array.isArray(v)) {
      if ('$value' in v) {
        out.push({
          path: [...prefix, k],
          value: v.$value,
          type: v.$type || null,
          description: v.$description || null,
        });
      } else {
        out.push(...flatten(v, [...prefix, k]));
      }
    }
  }
  return out;
}

/** Resolve {dot.path} refs against a flat map. Idempotent — loops until stable. */
function resolveRefs(tokens) {
  const byPath = Object.fromEntries(tokens.map((t) => [t.path.join('.'), t]));
  const REF = /\{([^{}]+)\}/g;

  function resolveValue(v, depth = 0) {
    if (depth > 12) throw new Error(`Ref depth exceeded at ${v}`);
    if (typeof v !== 'string') return v;
    if (!REF.test(v)) return v;
    REF.lastIndex = 0;
    return v.replace(REF, (_, ref) => {
      const target = byPath[ref];
      if (!target) throw new Error(`Unresolved ref: {${ref}}`);
      return resolveValue(target.value, depth + 1);
    });
  }

  for (const t of tokens) t.resolved = resolveValue(t.value);
  return tokens;
}

// ----------------------------------------------------------
// File writing helper
// ----------------------------------------------------------

function writeFile(relPath, content) {
  const full = path.join(BUILD_DIR, relPath);
  fs.mkdirSync(path.dirname(full), { recursive: true });
  fs.writeFileSync(full, content);
  console.log(`  ✓ ${relPath}`);
}

function cleanBuild() {
  fs.rmSync(BUILD_DIR, { recursive: true, force: true });
  fs.mkdirSync(BUILD_DIR, { recursive: true });
}

// ----------------------------------------------------------
// Naming
// ----------------------------------------------------------

/** path → --color-background-primary (strip "semantic" root) */
function cssVarName(p) {
  return '--' + p.filter((x) => x !== 'semantic').join('-');
}

/** camelCase from path parts. Numeric-first keys get prefix. */
function camel(parts, numericPrefix = '') {
  return parts
    .map((p, i) => {
      const clean = String(p).replace(/[^a-zA-Z0-9]/g, '');
      if (i === 0 && /^\d/.test(clean)) return numericPrefix + clean;
      if (i === 0) return clean.charAt(0).toLowerCase() + clean.slice(1);
      return clean.charAt(0).toUpperCase() + clean.slice(1);
    })
    .join('');
}

